// Algorithmic weekly report — no API needed
function scoreToGrade(score) {
  if (score >= 95) return 'A+'
  if (score >= 90) return 'A'
  if (score >= 85) return 'A-'
  if (score >= 80) return 'B+'
  if (score >= 75) return 'B'
  if (score >= 70) return 'B-'
  if (score >= 65) return 'C+'
  if (score >= 60) return 'C'
  if (score >= 50) return 'D'
  return 'F'
}

export function generateAlgoReport(stats, targets, weekStr) {
  const { workoutsCompleted, workoutRate, avgCal, avgProtein } = stats

  const calAdherence = avgCal && targets.calories
    ? Math.max(0, Math.round((1 - Math.abs(avgCal - targets.calories) / targets.calories) * 100))
    : 0
  const proAdherence = avgProtein && targets.protein
    ? Math.max(0, Math.round((1 - Math.abs(avgProtein - targets.protein) / targets.protein) * 100))
    : 0
  const macroScore = Math.round((calAdherence * 0.5) + (proAdherence * 0.5))
  const overallScore = Math.round((workoutRate * 0.6) + (macroScore * 0.4))

  const workoutGrade = scoreToGrade(workoutRate)
  const macroGrade = scoreToGrade(macroScore)
  const overallGrade = scoreToGrade(overallScore)

  // Build summary
  let summary = ''
  if (workoutRate >= 80 && macroScore >= 80) {
    summary = `Excellent week — you hit ${workoutsCompleted} workouts and your nutrition was on point. This is exactly the consistency that drives long-term results. Keep the momentum going.`
  } else if (workoutRate >= 60 && macroScore >= 60) {
    summary = `Solid week overall with ${workoutsCompleted} workouts completed. Your consistency is building a strong foundation. Small improvements in nutrition will compound quickly from here.`
  } else if (workoutRate >= 80) {
    summary = `Great training week — ${workoutsCompleted} sessions completed. Your workout consistency is strong. Dialing in your nutrition will unlock the next level of results.`
  } else if (macroScore >= 80) {
    summary = `Your nutrition tracking was excellent this week. Protein and calorie targets were well managed. Prioritize getting more workouts in next week to match that nutrition effort.`
  } else if (workoutsCompleted === 0 && avgCal === 0) {
    summary = `Nothing tracked this week — that's okay, every week is a fresh start. Open the app daily and log at least one thing. Small habits build big results.`
  } else {
    summary = `You finished ${workoutsCompleted} workouts and tracked some nutrition. Every bit of data helps you improve. Focus on showing up more consistently next week.`
  }

  // Build recommendation
  let recommendation = ''
  if (workoutRate < 60 && macroScore < 60) {
    recommendation = 'Pick 3 specific days this week and block them for workouts. Log every meal, even rough estimates count.'
  } else if (workoutRate < 60) {
    recommendation = 'Schedule your workouts like appointments. Aim for at least 4 sessions next week.'
  } else if (proAdherence < 60) {
    recommendation = `Hit your ${targets.protein}g protein target daily — add a protein shake or extra chicken to each meal.`
  } else if (calAdherence < 60) {
    recommendation = `Track your calories more closely. You're ${avgCal > targets.calories ? 'over' : 'under'} by ${Math.abs(avgCal - targets.calories)} kcal on average.`
  } else {
    recommendation = 'You\'re building great habits. Add one extra workout or increase intensity to break through your next plateau.'
  }

  return {
    overallGrade,
    workoutGrade,
    macroGrade,
    summary,
    recommendation,
    weekStr,
    stats,
    generatedAt: new Date().toISOString(),
  }
}

// Open Food Facts text search — free, no API key
export async function searchFood(query) {
  const url = `https://world.openfoodfacts.org/cgi/search.pl?search_terms=${encodeURIComponent(query)}&action=process&json=true&page_size=6&fields=product_name,nutriments,serving_quantity`
  const res = await fetch(url)
  const data = await res.json()
  const results = (data.products || [])
    .filter(p => p.product_name && p.nutriments?.['energy-kcal_100g'])
    .slice(0, 5)
    .map(p => {
      const n = p.nutriments
      const grams = p.serving_quantity ? Math.round(p.serving_quantity) : 100
      const ratio = grams / 100
      return {
        name: p.product_name,
        per100g: {
          calories: Math.round(n['energy-kcal_100g'] || 0),
          protein:  Math.round((n['proteins_100g'] || 0) * 10) / 10,
          carbs:    Math.round((n['carbohydrates_100g'] || 0) * 10) / 10,
          fat:      Math.round((n['fat_100g'] || 0) * 10) / 10,
        },
        defaultGrams: grams,
        calories: Math.round((n['energy-kcal_100g'] || 0) * ratio),
        protein:  Math.round((n['proteins_100g'] || 0) * ratio * 10) / 10,
        carbs:    Math.round((n['carbohydrates_100g'] || 0) * ratio * 10) / 10,
        fat:      Math.round((n['fat_100g'] || 0) * ratio * 10) / 10,
      }
    })
  return results
}
